import turtle
window = turtle.Screen()
window.setup(1000, 1000)
window.screensize(800, 800)
window.title("Lab 1")

turtle1 = turtle.Turtle()
turtle1.forward(250)
turtle1.right(90)
turtle1.forward(100)
turtle1.right(90)
turtle1.forward(250)
turtle1.right(90)
turtle1.forward(100)
window.mainloop()